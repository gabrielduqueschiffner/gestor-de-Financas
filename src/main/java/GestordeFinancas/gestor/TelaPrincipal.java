    private void refresh() {
        usuarioSelecionado = jComboBox1.getSelectedItem().toString();
        Usuario usuario = new Usuario(usuarioSelecionado);
        java.io.File diretorio = new java.io.File(path);
        java.io.File arquivoUsuario = new java.io.File(diretorio, usuario.getNome());
        float saldo = 0;
        try {

            FileReader fileReader = new FileReader(arquivoUsuario);
            BufferedReader bufferedReader = new BufferedReader(fileReader);

            String transacao = "";
            while ((transacao = bufferedReader.readLine()) != null) {
                if (!"".equals(transacao)) {
                    String[] parametros = transacao.split(",");
                    usuario.setExtrato(parametros);
                }
            }

            fileReader.close();
            bufferedReader.close();
        } catch (IOException e) {
        }
        DecimalFormat df = new DecimalFormat("0.00");
        jLabel9.setText("R$ " + df.format(usuario.getSaldo()));

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        for (int i = 0; i < model.getRowCount(); i++) {
            model.removeRow(i);
        }

        for (int i = 0; i < usuario.getExtrato().size(); i++) {
            if (usuario.getExtrato().get(i).getTipo() == 0) {
                model.addRow(new Object[]{usuario.getExtrato().get(i).getValor(), "Saque", usuario.getExtrato().get(i).getDescricao()});
            } else {
                model.addRow(new Object[]{usuario.getExtrato().get(i).getValor(), "Deposito", usuario.getExtrato().get(i).getDescricao()});
            }
        }
    }

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (!("Selecionar".equals(jComboBox1.getSelectedItem().toString()))) {
            if (!("".equals(jTextField3.getText()))) {
                
                java.io.File diretorio = new java.io.File(path);
                java.io.File arquivoUsuario = new java.io.File(diretorio, jComboBox1.getSelectedItem().toString());
                
                try {
                    FileReader fileReader = new FileReader(arquivoUsuario);
                    BufferedReader bufferedReader = new BufferedReader(fileReader);

                    String operacao = "";
                    String linha = "";
                    
                    while ((linha = bufferedReader.readLine()) != null) {
                        operacao = operacao + "\n" + linha;
                    }
                    
                    bufferedReader.close();
                    bufferedReader.close();
                    
                    FileWriter fileWriter = new FileWriter(arquivoUsuario, false);
                    PrintWriter printWriter = new PrintWriter(fileWriter);
                    
                    printWriter.write(operacao);
                    
                    
                    if (jRadioButton2.isSelected()) {
                        operacao = jTextField3.getText() + ",1," + jTextField2.getText();
                        printWriter.append("\n" + operacao);
                        printWriter.flush();
                        printWriter.close();
                    } else if (jRadioButton1.isSelected()) {
                        operacao = jTextField3.getText() + ",0," + jTextField2.getText();
                        printWriter.append("\n" + operacao);
                        printWriter.flush();
                        printWriter.close();
                    } else {
                        JOptionPane.showMessageDialog(null, "Preencha as informações corretamente!");
                    }
                } catch (IOException ex) {
                    Logger.getLogger(TelaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
                }

            } else {
                JOptionPane.showMessageDialog(null, "Preencha as informações corretamente!");
            }
            refresh();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um usuário");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

